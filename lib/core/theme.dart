## Brand & Style
The brand personality is a fusion of high-end editorial aesthetics and warm, Ugandan hospitality. It avoids the clinical coldness of medical spas in favor of a "Premium Boutique" feel. The UI leverages a **Modern Minimalist** style with **Tactile** influences, using the deep royal purples from the SalonGo logo — paired with soft lavender surfaces and a warm gold accent — to create a sense of calm luxury.

The experience should feel like stepping into a high-end Kampala salon: professional, culturally resonant, and meticulously curated. The logo's deep hexagon purple (`#4A1A8C`) anchors every primary interaction, while gold highlights draw the eye to key conversion points.

## Colors
The palette is built directly from the SalonGo logo — a deep navy-purple hexagon with a violet hair-flow gradient — extended into a full Material You-style system.

- **Primary (Logo Purple `#4A1A8C`):** The exact dominant purple from the hexagon logo. Used for primary buttons, active nav, headers, and all primary brand touchpoints.
- **Primary Container (`#6B2DBF`):** The mid-violet seen in the hair flow within the logo. Used for floating action buttons, selected chips, and highlighted cards.
- **Accent (Warm Gold `#F5A623`):** Not in the logo but the natural luxury complement to deep purple. Used exclusively for "Book Now" CTAs, star ratings, and premium badges.
- **Background (`#FFF8FF`):** A warm white with a faint purple cast — extends the logo's palette into surfaces without using pure white.
- **Text (`#1A0A3D`):** The deepest shade from the logo's navy shadow. Richer than charcoal, maintains brand DNA throughout all text.

## Typography
Unchanged — Montserrat + Inter pairing remains optimal for this brand positioning.

- **Headings:** Montserrat Bold — geometric confidence matching the hexagon geometry of the logo.
- **Body:** Inter 14px — functional clarity for service descriptions and booking details.
- **Labels:** Uppercase Inter with tracking — editorial, high-fashion feel for category tags.

## Layout & Spacing
Unchanged — 24px container padding, 8px spacing scale, 480px max content width.

## Elevation & Depth
Depth is communicated through **Ambient Shadows** tinted with the brand purple.

- **Surface Strategy:** Background sits on `#FFF8FF`. Interactive cards use pure `#FFFFFF` to pop forward.
- **Shadows:** `rgba(74, 26, 140, 0.06)` — a Deep Logo Purple tint that keeps shadows warm and brand-consistent.
- **Interactive Depth:** Cards scale to 0.98 on press with shadow tightening for tactile response.

## Shapes
Unchanged — 16px primary radius, 8px secondary. The hexagon logo already establishes a geometric identity; soft card corners provide the approachable contrast.

## Components
- **Primary Buttons:** `background: #4A1A8C` (logo purple), white text, 16px radius, min 52px height.
- **Book Now CTA:** `background: #F5A623` (gold), dark text `#3D2800`, full-width sticky bottom bar.
- **Service Cards:** White background, 16px radius, shadow `rgba(74,26,140,0.06) 0 4px 24px`.
- **Active Nav / Chips:** `background: #EDE5FF` (surface-container), text `#4A1A8C`.
- **Verified Stylist Badge:** Gold ring `border: 2px solid #F5A623` on circular avatar.
- **Rating Stars:** `color: #F5A623` gold — consistent with secondary accent.
- **Category Pills:** `background: rgba(74,26,140,0.08)`, text `#4A1A8C`, 8px radius.